    private void btn_outroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_outroActionPerformed
        String outroDado = JOptionPane.showInputDialog("Qual o n√∫mero de faces do dado?");
        if (outroDado != "" || outroDado != null){
            try{
                byte faces = Byte.parseByte(outroDado);
                dadoRpg.setNumeroFacetas((byte)faces);
                dadoRpg.sortearNumero();
                byte lucky = dadoRpg.mostrarNumero();
                Txt_Numero.setText(Byte.toString(lucky));
            } catch(NumberFormatException n){
                byte faces = 10;
                dadoRpg.setNumeroFacetas((byte)faces);
                dadoRpg.sortearNumero();
                byte lucky = dadoRpg.mostrarNumero();
                Txt_Numero.setText(Byte.toString(lucky));
            }
        }
    }//GEN-LAST:event_btn_outroActionPerformed

